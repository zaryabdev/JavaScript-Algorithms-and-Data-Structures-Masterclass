from five_sort.five_sort import five_sort

test_case = [
    ([12, 5, 1, 5, 12, 7],[12, 7, 1, 12, 5, 5]),
    ([5, 2, 5, 6, 5, 1, 10, 2, 5, 5],[2, 2, 10, 6, 1, 5, 5, 5, 5, 5]),
    ([5, 5, 5, 1, 1, 1, 4],[4, 1, 1, 1, 5, 5, 5]),
    ([5, 5, 6, 5, 5, 5, 5],[6, 5, 5, 5, 5, 5, 5]),
    ([5, 1, 2, 5, 5, 3, 2, 5, 1, 5, 5, 5, 4, 5],[4, 1, 2, 1, 2, 3, 5, 5, 5, 5, 5, 5, 5, 5]),
    ([5] * 20000+[4] * 20000, [4] * 20000 + [5] * 20000),
]

def test_five_sort():
    for test in test_case:
        assert five_sort(test[0]) == test[1]
